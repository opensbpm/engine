@EnableJpaRepositories(considerNestedRepositories = true,
        basePackages = {"org.opensbpm.engine.core"},
        repositoryBaseClass = DefaultJpaSpecificationRepository.class
)
@Configuration
public class SbpmJpaConfig {
}
